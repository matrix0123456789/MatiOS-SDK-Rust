use crate::gui::bitmap::Color;
use crate::gui::bitmap::Bitmap;
use alloc::string::String;
use crate::gui::controls::Control;
pub struct TextControl {
    pub text: String,
    pub color: Color
}
impl Control for TextControl {
    fn render(&self, pixel_ratio: f32) -> Bitmap {
        let mut ret = Bitmap::new(self.text.len()*9, 9, pixel_ratio);
        for i in 0..self.text.len() {
            TextControl::renderChar(self.text.chars().nth(i).unwrap(), &mut ret, i*9, 0, self.color);
        }
        return ret;
    }
}
